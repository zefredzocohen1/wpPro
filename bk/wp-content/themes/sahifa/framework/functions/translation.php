function __ti( $text ){
	$sanitize_text = tie_sanitize_title( htmlspecialchars ( $text ) );

	if( tie_get_option( $sanitize_text ) ){
		return htmlspecialchars_decode ( tie_get_option( $sanitize_text ) );
	}else{
		return __( $text , 'tie' );
	}
}


function _eti( $text ){
	echo  __ti( $text );
}

